import hashlib
import json
import logging
import re
import secrets
import string
import time
import uuid
from datetime import datetime, timezone
from pathlib import Path
from urllib.parse import parse_qs, urlencode, urljoin, urlparse

import requests
from bs4 import BeautifulSoup

log = logging.getLogger("apple-music-auth")

