"""
Apple Music Cookie Refresh Module
Performs SRP web login to obtain fresh cookies (including media-user-token).
Adapted from root main.py. Outputs Netscape-format cookies for gamdl.

Usage:
    from auth import refresh_cookies_sync, cookies_json_to_netscape

    cookies = refresh_cookies_sync(email, password)
    cookies_json_to_netscape(cookies, "/app/cookies/cookies.txt")
"""

